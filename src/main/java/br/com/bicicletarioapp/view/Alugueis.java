    private void carregarAlugueis() {
        EntityManager em = null;
        try {
            em = JPAUtil.getEntityManager();

            // CORREÇÃO: Use o repositório corretamente
            IAluguelRepository aluguelRepository = new AluguelRepository(em);
            AluguelService aluguelService = new AluguelService(em); // Passe o EntityManager

            List<br.com.bicicletarioapp.model.Aluguel> alugueis = aluguelService.listarAlugueis();

            // Limpa a tabela
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            // Define um formatador para a data e hora
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");

            // Preenche a tabela
            for (br.com.bicicletarioapp.model.Aluguel aluguel : alugueis) {
                model.addRow(new Object[]{
                    aluguel.getId(),
                    aluguel.getCliente().getId(),
                    aluguel.getBicicleta().getId(),
                    aluguel.getUsuario() != null ? aluguel.getUsuario().getId() : null,
                    aluguel.getHoraInicio().format(formatter),
                    aluguel.getHoraTermino() != null ? aluguel.getHoraTermino().format(formatter) : "Em andamento",
                    aluguel.getPrecoHora()
                });
            }

            for (int i = 0; i < 15; i++) {
                model.addRow(new Object[]{null, null, null, null, null, null});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar aluguéis: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } finally {
            if (em != null) {
                em.close();
            }
        }
    }
