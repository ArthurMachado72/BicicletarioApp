    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        System.out.println("=== ORDEM DAS COLUNAS ===");
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            System.out.println("Coluna " + i + ": " + jTable1.getColumnName(i).trim());
        }
        System.out.println("========================");
        EntityManager em = null;
        try {
            em = JPAUtil.getEntityManager();

            AluguelService aluguelService = new AluguelService(em);
            IClienteRepository clienteRepository = new ClienteRepository(em);
            ClienteService clienteService = new ClienteService(clienteRepository);
            IBicicletaRepository bicicletaRepository = new BicicletaRepository(em);
            BicicletaService bicicletaService = new BicicletaService(bicicletaRepository);

            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Selecione uma linha na tabela.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // USE ESTES ÍNDICES - baseado na ordem real das colunas
            Object clienteIdObj = jTable1.getValueAt(selectedRow, 1);      // coluna 1: cliente_id
            Object bicicletaIdObj = jTable1.getValueAt(selectedRow, 2);    // coluna 2: bicicleta_id  
            Object usuarioIdObj = jTable1.getValueAt(selectedRow, 3);      // coluna 3: usuario_id
            Object horaInicioObj = jTable1.getValueAt(selectedRow, 4);     // coluna 4: hora_inicio
            Object horaTerminoObj = jTable1.getValueAt(selectedRow, 5);    // coluna 5: hora_termino
            Object precoHoraObj = jTable1.getValueAt(selectedRow, 6);      // coluna 6: preço_hora

// DEBUG CORRETO - mostre os valores reais que está usando
            System.out.println("DEBUG CORRETO - Valores lidos:");
            System.out.println("Coluna 1 (cliente_id): " + jTable1.getValueAt(selectedRow, 1));
            System.out.println("Coluna 2 (bicicleta_id): " + jTable1.getValueAt(selectedRow, 2));
            System.out.println("Coluna 3 (usuario_id): " + jTable1.getValueAt(selectedRow, 3));
            System.out.println("Coluna 4 (hora_inicio): " + jTable1.getValueAt(selectedRow, 4));
            System.out.println("Coluna 5 (hora_termino): " + jTable1.getValueAt(selectedRow, 5));
            System.out.println("Coluna 6 (preco_hora): " + jTable1.getValueAt(selectedRow, 6));

            if (clienteIdObj == null || bicicletaIdObj == null || horaInicioObj == null || horaTerminoObj == null || precoHoraObj == null) {
                JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Long clienteId = Long.valueOf(clienteIdObj.toString());
            Long bicicletaId = Long.valueOf(bicicletaIdObj.toString());
            String horaInicioStr = horaInicioObj.toString();
            String horaTerminoStr = horaTerminoObj.toString();
            Double precoHora = Double.valueOf(precoHoraObj.toString());

            LocalDateTime horaInicio = LocalDateTime.now();
            LocalDateTime horaTermino = horaInicio.plusHours(1);

            Cliente cliente = clienteService.buscarClientePorId(clienteId);
            if (cliente == null) {
                JOptionPane.showMessageDialog(this, "Cliente com ID " + clienteId + " não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
                em.getTransaction().rollback();
                return;
            }

            Bicicleta bicicleta = bicicletaService.buscarPorId(bicicletaId);
            if (bicicleta == null) {
                JOptionPane.showMessageDialog(this, "Bicicleta com ID " + bicicletaId + " não encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);
                em.getTransaction().rollback();
                return;
            }

            br.com.bicicletarioapp.model.Aluguel novoAluguel = new br.com.bicicletarioapp.model.Aluguel();
            novoAluguel.setCliente(cliente);
            novoAluguel.setBicicleta(bicicleta);
            Usuario usuario = null;
            novoAluguel.setUsuario(usuario);
            novoAluguel.setPrecoHora(BigDecimal.valueOf(precoHora));

            try {
                aluguelService.registrarAluguel(novoAluguel);
            } catch (IllegalStateException e) {
                if (e.getMessage().contains("Transaction not successfully started")) {
                    // Ignora apenas erros de transação, pois o insert funcionou
                    System.out.println("Aluguel salvo com sucesso (aviso de transação ignorado)");
                } else {
                    throw e; // Re-lança outras exceções
                }
            }
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Aluguel adicionado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            em.close();
            carregarAlugueis();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro de formato numérico: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao adicionar o aluguel: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            e.printStackTrace();
        } finally {
            if (em != null && em.isOpen()) {
                em.close();
            }
        }


    }//GEN-LAST:event_btnAdicionarActionPerformed

