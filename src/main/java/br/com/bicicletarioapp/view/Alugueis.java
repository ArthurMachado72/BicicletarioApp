    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        EntityManager em = null;
        try {
            em = JPAUtil.getEntityManager();

            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Selecione um aluguel para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Obtenha o ID do aluguel selecionado (coluna 0: id)
            Object idObj = jTable1.getValueAt(selectedRow, 0);
            if (idObj == null) {
                JOptionPane.showMessageDialog(this, "Selecione um aluguel válido para excluir.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Long aluguelId = Long.valueOf(idObj.toString());

            // Confirmação antes de excluir
            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "Tem certeza que deseja excluir o aluguel ID: " + aluguelId + "?",
                    "Confirmar Exclusão",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            // Use o repositório diretamente para excluir
            IAluguelRepository aluguelRepository = new AluguelRepository(em);
            aluguelRepository.delete(aluguelId);

            JOptionPane.showMessageDialog(this, "Aluguel excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            // Recarregue a tabela
            carregarAlugueis();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID inválido selecionado.", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir aluguel: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } finally {
            if (em != null && em.isOpen()) {
                em.close();
            }

        }

    }//GEN-LAST:event_btnExcluirActionPerformed

